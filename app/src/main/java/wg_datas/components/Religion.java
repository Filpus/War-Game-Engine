package wg_datas.components;

public class Religion {
    private String name;
    private float happinessModifier;
    private float goldProductionModifier;
    private float foodProductionModifier;
    private float goldMaintenanceModifier;
    private float foodMaintenanceModifier;
    private float manpowerModifier;

    public String getName() {
        return name;
    }

    public float getHappinessModifier() {
        return happinessModifier;
    }

    public float getGoldProductionModifier() {
        return goldProductionModifier;
    }

    public float getFoodProductionModifier() {
        return foodProductionModifier;
    }

    public float getGoldMaintenanceModifier() {
        return goldMaintenanceModifier;
    }

    public float getFoodMaintenanceModifier() {
        return foodMaintenanceModifier;
    }

    public float getManpowerModifier() {
        return manpowerModifier;
    }
}
